function add(num1, num2) {
    return num1 + num2;
}

function subtract(num1, num2) {
    return num1 - num2;
}

function divide(num1, num2) {
    return num1 / num2;
}

function multiply(num1, num2) {
    return num1 * num2;
}

function sayHello(name) {
    const greeting = 'Hello';
    const greetingString = greeting + " " + name;
    return greetingString
}


